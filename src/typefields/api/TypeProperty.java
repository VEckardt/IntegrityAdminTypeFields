package typefields.api;

public class TypeProperty {

  public String propKey;
  public String propField;
  public String propFields;

  public TypeProperty (
		  String propKey,
		  String propField,
		  String propFields) {
	  this.propKey = propKey;
	  this.propField = propField;
	  this.propFields = propFields;	
  }
  
}
